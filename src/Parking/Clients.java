package Parking;
/**
 * This class is describing parameters of the clients;
 * @id - identity number in system;
 * @name - clients first and last name;
 * @countryCode - official country code;
 * @mobileNum - client mobile number;
 * @city - clients city;
 * @address - clients address;
 * @eMail - clients e-mail;
 * @password - clients password;
 * @bonuses - the number of bonuses the client has;
 * @carModel - model of clients car;
 * @carNum - clients car number
 *
 */
public class Clients {
    int id;
    String name;
    String countryCode;
    int mobileNum;
    String city;
    String address;
    String eMail;
    String password;
    int bonuses;
    String carModel;
    String carNum;

}
