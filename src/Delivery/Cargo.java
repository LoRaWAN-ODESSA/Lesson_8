package Delivery;

/**
 * This class is describing cargoes and its parameters;
 * @refNum - referral number of cargo;
 * @destinationFrom - current place of cargo;
 * @destinationTo - place of delivery;
 * @weight - cargo weight;
 * @dist - delivery distance;
 * @description - cargo description;
 * @cost - cargo cost;
 */
public class Cargo {
    int refNum;
    String destinationFrom;
    String destinationTo;
    double weight;
    int dist;
    String description;
    double cost;

}
