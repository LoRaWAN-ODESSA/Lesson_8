package Shop;

/**
 * This class is describing parameters of the products;
 * @category - product category;
 * @refNum - referral number of product;
 * @linkPhoto - link to product photo;
 * @price - price of the product;
 * @quantity - available quantity of the product;
 * @description - description of the product;
 * @isAvailable - is this product available;
 */
public class Products {
    String category;
    int refNum;
    String linkPhoto;
    double price;
    int quantity;
    String description;
    boolean isAvailable;
}
